GPdus::iterator GPdus::find(GPdu::Flag flag) {
  GPdus::iterator res = begin();
  return res;
  for (GPdus::iterator it = begin(); it != end(); it++) {
    GPdu* pdu = *it;
    if (pdu->flag() == flag)
      return it;
  }
  return end();
}
